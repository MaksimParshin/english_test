function shuffleArray(array) {
  for (let i = array.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [array[i], array[j]] = [array[j], array[i]];
  }
}

shuffleArray(words); // Перемешиваем массив слов

let currentWordIndex = 0;

function loadQuestion() {
  const word = words[currentWordIndex];
  const questionElement = document.getElementById("question");
  const optionsElement = document.getElementById("options");
  const resultElement = document.getElementById("result");

  questionElement.textContent = `Переведите слово: ${word.english}`;
  optionsElement.innerHTML = "";
  resultElement.textContent = "";

  const translations = words.map((w) => w.russian);
  const correctTranslation = word.russian;

  // Создаем три варианта ответа
  const options = [
    correctTranslation,
    ...getRandomTranslations(correctTranslation, translations),
  ].sort(() => Math.random() - 0.5);

  options.forEach((option) => {
    const div = document.createElement("div");
    div.textContent = option;
    div.className = "option";
    div.onclick = () => checkAnswer(option, correctTranslation);
    optionsElement.appendChild(div);
  });
}

function getRandomTranslations(correctTranslation, translations) {
  const filtered = translations.filter((t) => t !== correctTranslation);
  const randomTranslations = [];
  while (randomTranslations.length < 2) {
    const randomIndex = Math.floor(Math.random() * filtered.length);
    randomTranslations.push(filtered[randomIndex]);
    filtered.splice(randomIndex, 1);
  }
  return randomTranslations;
}

function checkAnswer(selected, correct) {
  const options = document.getElementsByClassName("option");
  const resultElement = document.getElementById("result");

  for (let option of options) {
    if (option.textContent === correct) {
      option.classList.add("correct");
    } else {
      option.classList.add("incorrect");
    }
  }

  if (selected === correct) {
    resultElement.textContent = "Правильно!";
  } else {
    resultElement.textContent = `Неправильно! Правильный ответ: ${correct}`;
  }

  currentWordIndex = (currentWordIndex + 1) % words.length;
  setTimeout(loadQuestion, 3000);
}

